// 变量声明

// 指定类型
var lang: String = "Swift"
var age: Int = 30
var a: Float = 2.0
var bool: Bool = true

println("String: \(lang), Int: \(age), Float: \(a), Bool: \(bool)")


// 不指定类型 
var name = "Swift"
var num = 19
var d = 7.0
var b = false

println("String: \(name), Int: \(num), Float: \(d), Bool: \(b)")


// 一行中声明多个变量
var x = 0, y = 0.0, z = 0
println("\(x), \(y), \(z)")
